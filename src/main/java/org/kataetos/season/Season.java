package org.kataetos.season;

/**
 * Seasons of the year, according to the rites of the Coptic Orthodox Church.
 *
 * @author pmansour
 */
public enum Season {

  // Special Periods
  Kiahk,
  HolyFiftyDays,
  Nayrouz,

  // Fasts
  NativityFast,
  JonahFast,
  // Start of Lent Monday to End of Lent Friday
  Lent,
  // Lazarus Saturday to Joyful Saturday (inclusive)
  PassionWeek,
  ApostlesFast,
  StMaryFast

}
